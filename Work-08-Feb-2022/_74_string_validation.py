"""
Python has built-in string validation methods for basic data. It can check if a string is composed of alphabetical characters,
alphanumeric characters, digits, etc.
"""
str="fhfjfj32333"
print(str.isupper())
print(str.islower())
print(str.isalpha())
print(str.isdigit())
print(str.isdecimal())
print(str.isnumeric())
print(str.isascii())
print(str.isidentifier())
print(str.istitle())